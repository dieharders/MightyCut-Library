// Build-time: inline the engine's fonts (woff2 → base64 data: URIs) into TS modules the
// engine injects at startup, so the browser engine renders with the real fonts WITHOUT the
// WebUI having to serve any font files (self-contained, mirrors the harness's inlineFonts).
//
// TWO TIERS, TWO MODULES — deliberately not one blob:
//   1. CORE (assets/fonts.css → src/engine/block-fonts.generated.ts, `CORE_FONTS_CSS`)
//      The chrome set (Space Grotesk / Inter / JetBrains Mono / Archivo Black) that EVERY
//      theme payload injects via core-fonts.ts. It is a superset of block's and future's
//      content fonts.
//   2. PER-THEME ADD-ONS (assets/fonts/<theme>-fonts.css → src/engine/<theme>-fonts.generated.ts,
//      `<THEME>_FONTS_CSS`) — the families a single theme needs BEYOND core (capsule → Bodoni
//      Moda). These stay in their own module so only that theme's lazy chunk carries the
//      ~130 KB of inlined woff2: register-<theme>.ts imports it, Vite code-splits it
//      (vite.config.ts `inlineDynamicImports: false`), and a block/future session never
//      downloads a face it cannot use. Folding them into CORE_FONTS_CSS would tax every deck.
//
// Adding a theme add-on is one ADDONS row + a register-<theme>.ts that injects it (and the
// theme-parity "font coverage" tripwire's ADDON_FONT_CSS map, which reads the module emitted
// here — the same bytes the browser injects). standard → Playfair is staged in assets/fonts/
// but NOT wired here: it is not a live component theme yet.
//
// Re-run by `build:engine`/`test`/`typecheck`'s pre-step (`pnpm gen:fonts`).
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
// Canonical font source: assets/fonts/*.woff2 + assets/fonts.css (core chrome) +
// assets/fonts/<theme>-fonts.css (per-theme add-ons).
const assetsDir = path.join(root, "assets");
const engineDir = path.join(root, "src", "engine");

/**
 * Read a font stylesheet and swap every `url(...)` for a base64 data: URI.
 *
 * Replace ONLY the url(...) — the source CSS already carries its own `format("woff2")`
 * after each url(), so appending another here would produce an invalid
 * `... format("woff2") format("woff2")` src and the @font-face would be dropped.
 *
 * Each woff2 reference is resolved against the DIRECTORY OF ITS OWN STYLESHEET (the same
 * rule a browser applies), because the two tiers reference their files differently:
 * assets/fonts.css says `fonts/<name>.woff2` while assets/fonts/<theme>-fonts.css sits
 * beside its faces and says a bare `<name>.woff2`. Resolving both from assets/ would break
 * the add-ons; resolving both from assets/fonts/ would break core.
 */
const inlineFontCss = (cssPath) => {
  const baseDir = path.dirname(cssPath);
  return fs.readFileSync(cssPath, "utf8").replace(
    /url\(["']?([^"')]+\.woff2)["']?\)/g,
    (_m, file) => {
      const b64 = fs.readFileSync(path.join(baseDir, file)).toString("base64");
      return `url("data:font/woff2;base64,${b64}")`;
    },
  );
};

/** Write a generated module and log it (same shape for core and every add-on). */
const emit = (outFile, banner, constName, css) => {
  const out = `// GENERATED by scripts/gen-inline-fonts.mjs — do not edit.
${banner}
export const ${constName} = ${JSON.stringify(css)};
`;
  const outPath = path.join(engineDir, outFile);
  fs.writeFileSync(outPath, out);
  console.log(`wrote ${path.relative(root, outPath)} (${out.length} chars)`);
};

// ------------------------------------------------------------------------ core ---
emit(
  "block-fonts.generated.ts",
  `// The core chrome fonts, woff2 base64-inlined as data: URIs, so the engine is
// self-contained (no font files for the WebUI to serve). Covers every live theme's
// content fonts EXCEPT the per-theme add-ons (see <theme>-fonts.generated.ts).`,
  "CORE_FONTS_CSS",
  inlineFontCss(path.join(assetsDir, "fonts.css")),
);

// ------------------------------------------------------------- theme add-ons ---
// theme name → the stylesheet in assets/fonts/ holding the faces that theme needs beyond
// core. Emits src/engine/<theme>-fonts.generated.ts exporting <THEME>_FONTS_CSS.
const ADDONS = [
  { theme: "capsule", css: "capsule-fonts.css" },
  { theme: "professional", css: "professional-fonts.css" },
];

for (const { theme, css } of ADDONS) {
  const constName = `${theme.toUpperCase().replace(/-/g, "_")}_FONTS_CSS`;
  emit(
    `${theme}-fonts.generated.ts`,
    `// ${theme}'s ADD-ON content faces (beyond the core chrome set), woff2 base64-inlined as
// data: URIs. Injected ONLY by register-${theme}.ts, so this payload rides ${theme}'s lazy
// theme chunk and no other theme downloads it. Source: assets/fonts/${css}.`,
    constName,
    inlineFontCss(path.join(assetsDir, "fonts", css)),
  );
}
