// Build-time: inline the block theme fonts (woff2 → base64 data: URIs) into a TS
// module the engine injects at startup, so the browser engine renders with the
// real theme fonts WITHOUT the WebUI having to serve any font files (self-contained,
// mirrors the harness's inlineFonts). Re-run by `build:engine`'s pre-step.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const fontsDir = path.join(root, "assets", "themes", "block", "fonts");
const cssPath = path.join(fontsDir, "theme-fonts.css");

const css = fs.readFileSync(cssPath, "utf8").replace(
  /url\(["']?([^"')]+\.woff2)["']?\)/g,
  (_m, file) => {
    const b64 = fs.readFileSync(path.join(fontsDir, file)).toString("base64");
    return `url("data:font/woff2;base64,${b64}") format("woff2")`;
  },
);

const out = `// GENERATED by scripts/gen-inline-fonts.mjs — do not edit.
// The block theme fonts, woff2 base64-inlined as data: URIs, so the engine is
// self-contained (no font files for the WebUI to serve).
export const BLOCK_FONTS_CSS = ${JSON.stringify(css)};
`;
const outPath = path.join(root, "src", "engine", "block-fonts.generated.ts");
fs.writeFileSync(outPath, out);
console.log(`wrote ${path.relative(root, outPath)} (${out.length} chars)`);
