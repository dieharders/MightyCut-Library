// Build-time: inline the CORE chrome fonts (woff2 → base64 data: URIs) into a TS
// module the engine injects at startup, so the browser engine renders with the real
// fonts WITHOUT the WebUI having to serve any font files (self-contained, mirrors the
// harness's inlineFonts). The core set (Space Grotesk / Inter / JetBrains Mono /
// Archivo Black) is the single canonical font source in assets/fonts/ and is a
// superset of every currently-live theme's content fonts (block/creative/future/
// professional). Themes that add a family beyond core (capsule → Bodoni, standard →
// Playfair) ship their own <theme>-fonts.css alongside; wire those in here when they
// go live in the WebUI. Re-run by `build:engine`'s pre-step.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
// Canonical font source: assets/fonts/*.woff2 + assets/fonts.css (core chrome).
const assetsDir = path.join(root, "assets");
const cssPath = path.join(assetsDir, "fonts.css");

// Replace ONLY the url(...) with a data: URI — the source CSS already carries its
// own `format("woff2")` after each url(), so appending another here would produce an
// invalid `... format("woff2") format("woff2")` src and the @font-face would be dropped.
// fonts.css references `fonts/<name>.woff2` (relative to assets/), so resolve from assetsDir.
const css = fs.readFileSync(cssPath, "utf8").replace(
  /url\(["']?([^"')]+\.woff2)["']?\)/g,
  (_m, file) => {
    const b64 = fs.readFileSync(path.join(assetsDir, file)).toString("base64");
    return `url("data:font/woff2;base64,${b64}")`;
  },
);

const out = `// GENERATED by scripts/gen-inline-fonts.mjs — do not edit.
// The core chrome fonts, woff2 base64-inlined as data: URIs, so the engine is
// self-contained (no font files for the WebUI to serve). Superset of every live
// theme's content fonts.
export const CORE_FONTS_CSS = ${JSON.stringify(css)};
`;
const outPath = path.join(root, "src", "engine", "block-fonts.generated.ts");
fs.writeFileSync(outPath, out);
console.log(`wrote ${path.relative(root, outPath)} (${out.length} chars)`);
